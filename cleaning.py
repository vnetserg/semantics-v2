def clean_data(data, groupings, splits, drops):
    all_clusters = set()
    data_cl = data.copy()
    for group in groupings:
        group_name = choose_group_name(group)
        for cluster in group:
            if cluster in all_clusters:
                raise ValueError(cluster)
                all_clusters.add(cluster)
            if cluster != group_name:
                data_cl.replace(cluster, group_name, inplace=True)
    for i, ids in enumerate(splits):
        group = "SPLIT_" + str(i)
        if type(ids) not in (tuple, list):
            ids = (ids,)
        for id in ids:
            data_cl.loc[id]["cluster"] = group
    data_cl = data_cl[~data_cl["cluster"].isin(drops)]
    return data_cl

def choose_group_name(group):
    candidates = [clus for clus in group if "NONE" not in clus]
    if not candidates:
        return group[0]
    return candidates[0]

